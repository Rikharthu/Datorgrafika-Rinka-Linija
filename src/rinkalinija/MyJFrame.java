    private void drawElipse(int xc,int yc, int rx,int ry){
        Graphics g= canvasPanel.getGraphics();
        
        int ry2,rx2,i,xn,yn;
        float pn;
        
        
        
        i=0;
        xn=0;       // шаг
        yn=ry;      
        ry2=ry*ry;  // типа в квадрате
        rx2=rx*rx;  
        pn=(float)(ry2-rx2*ry+.25*rx2);
        
        // DEBUG 1
        g.setColor(Color.GREEN);
        g.drawLine(xc,yc,xn+xc,ry+yc);
        
        while((ry2*xn)<=(rx2*yn)){ // TODO что собой представляет?
            xn++;
            if(pn<0){
                // zem punkta
//                xn++;
                pn=pn+2*ry2*xn+ry2;
            }else{
//                xn++;
                yn--;
                pn=pn+2*ry2*xn+ry2-2*rx2*yn;
            }
            g.setColor(Color.RED);
            g.drawLine(xn+xc, yn+yc, xn+xc, yn+yc);
            g.setColor(Color.GRAY);
            drawSymmetric(xn,yn,xc,yc,g);
            logArea.append(String.format("1. Part %d.)\txn: %d yn:%d pn: %f\n",++i,xn,yn,pn));
        }
        //DEBUG 2
        g.setColor(Color.GREEN);
        g.drawLine(xc,yc,xn+xc,yn+yc);
        
        i=0; // reset counter
        pn=(float)(ry2*(xn+.5)*(xn+.5) 
                + rx2*(yn-1)*(yn-1) 
                - rx2*ry2);
        
        logArea.append("\n");
        
        while(yn>0){
            
            if(pn>0){
                yn--;
                pn=pn-2*rx2*yn+rx2;
            }else{
                yn--;
                xn++;
                pn=pn-2*rx2*yn+rx2+2*ry2*xn;
            }          
            g.setColor(Color.BLUE);
            g.drawLine(xn+xc, yn+yc, xn+xc, yn+yc);
            // draw other points
            g.setColor(Color.GRAY);
            drawSymmetric(xn,yn,xc,yc,g);
            logArea.append(String.format("2. Part %d.)\txn: %d yn:%d pn: %f\n",++i,xn,yn,pn));
        }
        //DEBUG
        g.setColor(Color.GREEN);
        g.drawLine(xc,yc,xn+xc,yn+yc);
    }
    
