    private void canvasPanelMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_canvasPanelMouseReleased
        // TODO add your handling code here:
        sX2=evt.getX();
        sY2=evt.getY();
        
        int xc= (sX2+sX1)/2;
        int yc= (sY2+sY1)/2;
        int rx=Math.abs((sX2-sX1)/2);
        int ry=Math.abs((sY1-sY2)/2);
        drawElipse(xc,yc,rx,ry);
    }//GEN-LAST:event_canvasPanelMouseReleased
    
    /** Draws symmetrical points */
    private void drawSymmetric(int xn,int yn,int xc,int yc,Graphics g){
        // simetriski pret OY
        g.drawLine(xn+xc, -yn+yc, xn+xc, -yn+yc);
        // simetriski pret OX un OY
        g.drawLine(-xn+xc, -yn+yc, -xn+xc, -yn+yc);
        // simetriski pret OX
        g.drawLine(-xn+xc, yn+yc, -xn+xc, yn+yc);
    }
    
